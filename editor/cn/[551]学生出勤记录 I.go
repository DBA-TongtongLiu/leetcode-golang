func checkRecord(s string) bool {
	var numA, consL int
	for _, ss := range []byte(s) {
		if string(ss) == "A" {
			numA++
			if numA > 1 {
				return false
			}
			consL = 0
			continue
		}
		if string(ss) == "L" {
			consL++
			if consL > 2 {
				return false
			}
			continue
		}
		consL = 0
	}
	return true
}

