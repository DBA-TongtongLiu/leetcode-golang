func distributeCandies(candyType []int) int {
	candyMp := make(map[int]int)
	for _, c := range candyType {
		if v, ok := candyMp[c]; ok {
			candyMp[c] = v + 1
		} else {
			candyMp[c] = 1
		}
	}
	candyNum := len(candyType)
	typeNum := len(candyMp)
	if typeNum >= candyNum/2 {
		return candyNum / 2
	} else {
		return typeNum
	}
	return 0

}

