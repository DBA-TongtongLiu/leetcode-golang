func massage(nums []int) int {
	resList := []int{0}
	max, val := 0, 0
	for i, n := range nums {
		if i == 0 || i == 1 {
			val = n
		} else {
			val = getMax(resList[0:i]) + n
		}
		if val > max {
			max = val
		}
		resList = append(resList, val)
	}
	return max
}

func getMax(nums []int) int {
	max := 0
	for _, n := range nums {
		if n > max {
			max = n
		}
	}
	return max
}

