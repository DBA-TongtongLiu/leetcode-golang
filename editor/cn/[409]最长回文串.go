func longestPalindrome(s string) int {
	m := make(map[int32]int)
	for _, i := range s {
		if v, ok := m[i]; ok {
			m[i] = v + 1
		} else {
			m[i] = 1
		}
	}
	var max int
	var flag bool
	for _, v := range m {
		if v%2 == 1 {
			flag = true
			v = v - 1
		}
		max += v
	}
	if flag {
		max++
	}
	return max
}

